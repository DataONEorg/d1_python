# Stdlib.
import logging
#import os
import sys
import unittest

# D1.
sys.path.append('../fuse')
import filename_extensions

# Set up logger for this module.
log = logging.getLogger(__name__)


class TestFilenameExtension(unittest.TestCase):
  def setUp(self):
    pass

  def test_100_map(self):
    ext = filename_extensions.map_object_format_to_filename_extension(
      'eml://ecoinformatics.org/eml-2.0.0'
    )
    self.assertEqual(ext, '.xml')

#===============================================================================


def log_setup():
  formatter = logging.Formatter(
    '%(asctime)s %(levelname)-8s %(message)s', '%y/%m/%d %H:%M:%S'
  )
  console_logger = logging.StreamHandler(sys.stdout)
  console_logger.setFormatter(formatter)
  logging.getLogger('').addHandler(console_logger)


def main():
  import optparse

  log_setup()

  # Command line opts.
  parser = optparse.OptionParser()
  parser.add_option('--debug', action='store_true', default=False, dest='debug')
  parser.add_option(
    '--test', action='store',
    default='',
    dest='test',
    help='run a single test'
  )

  (options, arguments) = parser.parse_args()

  if options.debug:
    logging.getLogger('').setLevel(logging.DEBUG)
  else:
    logging.getLogger('').setLevel(logging.ERROR)

  s = TestFilenameExtension
  s.options = options

  if options.test != '':
    suite = unittest.TestSuite(map(s, [options.test]))
  else:
    suite = unittest.TestLoader().loadTestsFromTestCase(s)

  unittest.TextTestRunner(verbosity=2).run(suite)


if __name__ == '__main__':
  main()
